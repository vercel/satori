/**
 * Reads the Tailwind CSS v4 default stylesheet from node_modules,
 * strips the @layer base block (browser reset / preflight — not needed for satori),
 * and writes a TypeScript constant to src/tw/tailwind-css.ts.
 *
 * Usage:  node scripts/generate-tailwind-css.mjs
 */

import { readFileSync, writeFileSync } from 'fs';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, '..');

const raw = readFileSync(
	resolve(root, 'node_modules/tailwindcss/index.css'),
	'utf8'
);

// Strip @layer base { ... } using balanced-brace matching
const stripLayerBase = css => {
	const marker = '@layer base';
	const start = css.indexOf(marker);

	if (start === -1) {
		return css;
	}

	const openBrace = css.indexOf('{', start);

	if (openBrace === -1) {
		return css;
	}

	let depth = 1;
	let i = openBrace + 1;

	while (i < css.length && depth > 0) {
		if (css[i] === '{') {
			depth++;
		}
		if (css[i] === '}') {
			depth--;
		}
		i++;
	}

	return css.substring(0, start) + css.substring(i);
};

const cleaned = stripLayerBase(raw)
	.replace(/\n{3,}/g, '\n\n')
	.trim();

const escaped = cleaned
	.replace(/\\/g, '\\\\')
	.replace(/`/g, '\\`')
	.replace(/\$/g, '\\$');

const output = `// Auto-generated by scripts/generate-tailwind-css.mjs — do not edit manually
const TAILWIND_CSS = \`${escaped}\`;

export { TAILWIND_CSS };
`;

const outPath = resolve(root, 'src/tw/tailwind-css.ts');
writeFileSync(outPath, output, 'utf8');
console.log(`Wrote ${outPath}`);
